package neto.estudos.firstweek;

public class TiposEVariaveis {
    public static void main(String[] args) {
        double salarioMinimo = 1420;
        // Usando a variável do tipo float, deve-se colocar um F no final do número usado
        // Exemplo: float pi = 3.14F;
        System.out.println("O salário mínimo atual é de R$" + salarioMinimo + " reais");
    }
}
